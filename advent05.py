
def get_params(input, eip):
	params = []

	cur_instruction = input[eip] % 100

	param_count = 0
	if cur_instruction in [1, 2, 7, 8]:
		param_count = 3
	elif cur_instruction in [5, 6]:
		param_count = 2
	elif cur_instruction in [3, 4]:
		param_count = 1

	modes = input[eip] // 100

	cur_idx = 1
	while cur_idx <= param_count:
		if modes % 10 == 1:
			params.append(input[eip + cur_idx])
		else:
			params.append(input[input[eip + cur_idx]])
		cur_idx += 1
		modes = modes // 10

	return params

